class TestRejectsMalformedFile:
    def test_random_columns_raises_value_error(self, reader, tmp_path):
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Garbage"
        ws.append(["Foo", "Bar", "Baz", "Qux", "Frob", "Nicknack"])
        ws.append([1, 2, 3, 4, 5, 6])
        ws.append([7, 8, 9, 10, 11, 12])
        path = tmp_path / "garbage.xlsx"
        wb.save(path)
        wb.close()

        with pytest.raises(ValueError) as exc:
            reader.read(path, run_id="bad")
        # Error message must enumerate what was seen + what was tried.
        msg = str(exc.value)
        assert "missing" in msg.lower() or "no sheet" in msg.lower()

    def test_partial_columns_lists_missing_fields(self, reader, tmp_path):
        """A sheet with a couple of canonical columns but not all required ones
        should produce a ValueError that names what was missing."""
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Half"
        ws.append([
            "Abs. Distance, m", "Joint Number", "Wall Thickness, mm",
            "Depth, %", "Length, mm",  # missing width + surface
        ])
        ws.append([100.0, 10, 8.7, 25.0, 30])
        path = tmp_path / "half.xlsx"
        wb.save(path)
        wb.close()

        with pytest.raises(ValueError) as exc:
            reader.read(path, run_id="bad")
        msg = str(exc.value).lower()
        # Must mention at least one of the missing fields.
        assert "width_mm" in msg or "surface" in msg

    def test_nonexistent_file_raises_filenotfound(self, reader):
        with pytest.raises(FileNotFoundError):
            reader.read(EXAMPLES / "this_file_does_not_exist.xlsx", run_id="x")

    def test_garbage_magic_bytes_raises(self, reader, tmp_path):
        bogus = tmp_path / "fake.xlsx"
        bogus.write_bytes(b"\x00\x01\x02\x03GARBAGE")
        with pytest.raises(ValueError, match="unrecognised file format"):
            reader.read(bogus, run_id="x")


