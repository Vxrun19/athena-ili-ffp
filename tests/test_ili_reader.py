class TestAbuRoadDentLeak:
    """Real-data regression: feature 1637 in the Abu Road Run-2 file must
    be filtered out of features_for_assessment() — it's a dent, not a
    metal-loss feature, and the FFP engine isn't valid for it."""

    @pytest.fixture(scope="class")
    def run(self):
        return ILIReader().read(str(_ABU_ROAD_RUN2), run_id="run_2")

    def test_feature_1637_in_raw_features(self, run):
        """Sanity: the row IS present in run.features."""
        f1637 = next(
            (f for f in run.features if f.anomaly_id == "1637"), None,
        )
        assert f1637 is not None, (
            "Feature 1637 not in raw features at all — sheet/column "
            "resolution may have changed."
        )

    def test_feature_1637_is_a_dent(self, run):
        """The fid must resolve to DENT after value normalisation."""
        f1637 = next(
            (f for f in run.features if f.anomaly_id == "1637"), None,
        )
        assert f1637 is not None
        assert f1637.feature_identification == FeatureIdentification.DENT, (
            f"got fid = {f1637.feature_identification!r} "
            f"(raw description = {f1637.raw_description!r})"
        )

    def test_feature_1637_not_in_features_for_assessment(self, run):
        """The whole point of this regression test."""
        assess_ids = {f.anomaly_id for f in run.features_for_assessment()}
        assert "1637" not in assess_ids, (
            "Feature 1637 is a dent and must not be FFP-assessed. "
            "If you see this fail, check value_normalisations.feature_"
            "identification in config/column_synonyms.yaml for the "
            "actual vendor string used on row 1637."
        )
