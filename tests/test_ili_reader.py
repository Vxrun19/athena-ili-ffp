@pytest.fixture(scope="module")
def reader() -> ILIReader:
    return ILIReader()


# Shared cache so individual cross-cutting tests below don't re-read the same
# 136k-row file. Keyed by filename.
_CACHE: dict[str, object] = {}


def _read_cached(reader: ILIReader, fname: str):
    if fname not in _CACHE:
        path = EXAMPLES / fname
        if not path.exists():
            pytest.skip(f"example file not present: {fname}")
        _CACHE[fname] = reader.read(path, run_id=fname[:8])
    return _CACHE[fname]


@pytest.fixture(scope="module", params=sorted(EXPECTED.keys()))
def parsed(request, reader) -> tuple[str, "ILIRun", dict]:
    fname = request.param
    run = _read_cached(reader, fname)
    return fname, run, EXPECTED[fname]


