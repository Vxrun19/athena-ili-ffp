class TestFeaturesForAssessment:
    """Cluster-aware accessor used for FFP counts."""

    def test_children_excluded(self, reader):
        # 1YCF: PDF says 29,479 children → 106,969 assessable. Reader
        # gets 29,479 children (exact) and 106,969 assessable (exact).
        # Note: post-dent-leak fix, total - assess includes BOTH cluster
        # children AND non-metal-loss features (dents / welds / cracks),
        # so we count children directly via cluster_parent_id.
        run = _read_cached(reader, "1YCF_Pipeline_Listing__run2_.xlsx")
        children = sum(
            1 for f in run.features if f.cluster_parent_id is not None
        )
        assert children == 29479, f"expected 29,479 cluster children, got {children}"

    def test_parents_kept(self, reader):
        run = _read_cached(reader, "1YCF_Pipeline_Listing__run2_.xlsx")
        # Every COCL parent must survive the filter.
        parents = [f for f in run.features if f.is_cluster_parent]
        assert parents, "no cluster parents found"
        assess_ids = {id(f) for f in run.features_for_assessment()}
        for p in parents:
            assert id(p) in assess_ids, f"parent {p.anomaly_id!r} dropped from assessment set"

    def test_no_children_when_no_description_column(self, reader):
        # NGP 2019 single-sheet has no Description column, so the reader
        # can't identify children — assessment count equals total.
        run = _read_cached(reader, "8-9100-13964_28IP1IP2_Pipe_Tally_run1_.xlsx")
        assert len(run.features_for_assessment()) == len(run.features)

    def test_assessment_subset_of_total(self, reader):
        for fname in EXPECTED:
            run = _read_cached(reader, fname)
            assess = run.features_for_assessment()
            assert len(assess) <= len(run.features)
            # No child snuck into the assessment set.
            assert all(f.cluster_parent_id is None for f in assess)


