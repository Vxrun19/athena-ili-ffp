class TestDentFiltering:
    """Confirm dents never reach features_for_assessment().

    Real-world bug: feature 1637 in the Abu Road 1ZYC file was tagged
    "Dent complex" / DENC and slipped into FFP because the value-
    normalisation map didn't include "Dent complex". The reader treated
    the row as UNDEFINED, which doesn't match _NON_METAL_LOSS_FIDS, and
    the FFP engine then interpreted the 0.9 %OD depth as 90 %WT,
    producing ERF = 8.57 (vs the published max ERF ~0.9).
    """

    @pytest.mark.parametrize("dent_label", [
        # Strings that real vendor files have produced — all must
        # normalise to DENT and therefore be filtered.
        "Dent complex",
        "Dent plain",
        "dent kinked",
        "DENC",
        "DENP",
        "DENK",
        "Dent with additional feature",
    ])
    def test_dent_labels_normalise_to_DENT_enum(self, dent_label, tmp_path):
        """Build a tiny synthetic NGP-style sheet and confirm the reader
        normalises every dent variant to FeatureIdentification.DENT,
        and that all such rows are filtered out of
        features_for_assessment()."""
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Defects"
        # Header row + one CORR row + one dent row. The reader needs ≥3
        # canonical-field hits to pick the sheet — we provide plenty.
        ws.append([
            "Anomaly ID", "Absolute Distance, m", "Joint Number",
            "WT, mm", "Depth, %WT", "Length, mm", "Width, mm",
            "Surface", "POF Acronym", "Description",
        ])
        ws.append([
            "1", 100.0, 1, 7.1, 25.0, 50.0, 20.0,
            "Internal", "CORR", "metal loss",
        ])
        ws.append([
            "1637", 200.0, 2, 7.1, 0.9, 50.0, 20.0,
            "Internal", dent_label, "area of dent",
        ])
        pdf_path = tmp_path / "dent_test.xlsx"
        wb.save(pdf_path)

        reader = ILIReader()
        run = reader.read(str(pdf_path), run_id="run_test")

        # Both rows should be in the raw feature list.
        assert len(run.features) >= 2, (
            f"only {len(run.features)} features parsed — sheet detection "
            "or column resolution failed"
        )

        # Find feature 1637 — confirm it exists and is tagged DENT.
        f1637 = next(
            (f for f in run.features if f.anomaly_id == "1637"), None,
        )
        assert f1637 is not None, "feature 1637 missing from raw features"
        assert f1637.feature_identification == FeatureIdentification.DENT, (
            f"vendor label {dent_label!r} should normalise to DENT, got "
            f"{f1637.feature_identification!r}"
        )

        # And it must NOT be in features_for_assessment().
        assess_ids = {f.anomaly_id for f in run.features_for_assessment()}
        assert "1637" not in assess_ids, (
            f"DENT feature 1637 (vendor label {dent_label!r}) leaked into "
            "features_for_assessment() — the non_metal_loss filter is "
            "missing this code path"
        )

    def test_unknown_dent_string_produces_parse_warning(self, tmp_path):
        """When a vendor string CAN'T be normalised but looks dent-like,
        the reader records an UNRECOGNISED_NON_ML_FID parse warning so a
        maintainer notices and extends column_synonyms.yaml."""
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Defects"
        ws.append([
            "Anomaly ID", "Absolute Distance, m", "Joint Number",
            "WT, mm", "Depth, %WT", "Length, mm", "Width, mm",
            "Surface", "POF Acronym",
        ])
        ws.append([
            "1", 100.0, 1, 7.1, 25.0, 50.0, 20.0, "Internal", "CORR",
        ])
        ws.append([
            "9999", 200.0, 2, 7.1, 0.9, 50.0, 20.0,
            "Internal", "Dent with weird hyperspace anomaly",  # unknown variant
        ])
        path = tmp_path / "unknown_dent.xlsx"
        wb.save(path)

        run = ILIReader().read(str(path), run_id="run_test")
        warnings = [w for w in run.parse_warnings if "UNRECOGNISED_NON_ML_FID" in w]
        assert warnings, (
            "expected an UNRECOGNISED_NON_ML_FID warning for unknown dent "
            f"variant; got parse_warnings = {run.parse_warnings}"
        )


