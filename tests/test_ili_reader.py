class TestLatLonChecks:
    """Reader emits QA flags for coordinate sanity, configurable per project."""

    def test_swap_flag_emitted_and_values_fixed(self, reader):
        run = _read_cached(
            reader, "10_inch_Kandla_to_Samakhiali__58_2_km_FR_Pipe_Tally_Rev_0.xlsx"
        )
        codes = [f.code for f in run.qa_flags]
        assert QAFlagCode.COORDINATES_SWAPPED in codes, (
            f"expected COORDINATES_SWAPPED in {codes}"
        )
        # After swap: lat ~ 22-24°, lon ~ 69-77° (India).
        lats = [f.latitude for f in run.features if f.latitude is not None]
        lons = [f.longitude for f in run.features if f.longitude is not None]
        assert lats and lons
        assert 20 < max(lats) < 30, f"lat range looks wrong: max={max(lats)}"
        assert 65 < max(lons) < 80, f"lon range looks wrong: max={max(lons)}"

    def test_well_oriented_file_emits_no_lat_lon_flag(self, reader):
        # 1YCF is well-oriented (lat ~23, lon ~70) — no flag expected.
        run = _read_cached(reader, "1YCF_Pipeline_Listing__run2_.xlsx")
        codes = [f.code for f in run.qa_flags]
        assert QAFlagCode.COORDINATES_SWAPPED not in codes
        assert QAFlagCode.LAT_LON_OUT_OF_BOUNDS not in codes

    def test_out_of_bounds_flag_when_coords_make_no_sense(self, tmp_path):
        """Build a tiny synthetic file whose coords fall outside both
        orientations of the India box — reader must emit
        LAT_LON_OUT_OF_BOUNDS (not silently swap or accept)."""
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Defects"
        ws.append([
            "Abs. Distance, m", "Joint Number", "WT, mm", "Depth, %",
            "Length, mm", "Width, mm", "Surface", "POF Acronym",
            "Latitude", "Longitude",
        ])
        # Atlantic Ocean coords (lat 0, lon -30) — far from India and from any
        # swap that would still hit India.
        for i in range(5):
            ws.append([i * 10.0, 10 + i, 8.7, 25.0, 30.0, 40.0, "int.", "CORR", 0.0, -30.0])
        path = tmp_path / "oob.xlsx"
        wb.save(path)
        wb.close()

        run = ILIReader().read(path, run_id="oob")
        codes = [f.code for f in run.qa_flags]
        assert QAFlagCode.LAT_LON_OUT_OF_BOUNDS in codes, codes
        # Crucially: no silent swap happened.
        assert all(f.latitude == 0.0 for f in run.features)
        assert all(f.longitude == -30.0 for f in run.features)

    def test_coordinate_bounds_override(self, tmp_path):
        """Passing a custom bounding box should accept the same file that
        would otherwise be OOB."""
        import openpyxl
        wb = openpyxl.Workbook()
        ws = wb.active
        ws.title = "Defects"
        ws.append([
            "Abs. Distance, m", "Joint Number", "WT, mm", "Depth, %",
            "Length, mm", "Width, mm", "Surface", "POF Acronym",
            "Latitude", "Longitude",
        ])
        for i in range(5):
            ws.append([i * 10.0, 10 + i, 8.7, 25.0, 30.0, 40.0, "int.", "CORR", 0.0, -30.0])
        path = tmp_path / "atlantic.xlsx"
        wb.save(path)
        wb.close()

        run = ILIReader(
            coordinate_bounds={"lat": [-10, 10], "lon": [-40, -20]}
        ).read(path, run_id="r")
        codes = [f.code for f in run.qa_flags]
        assert QAFlagCode.LAT_LON_OUT_OF_BOUNDS not in codes
        assert QAFlagCode.COORDINATES_SWAPPED not in codes


