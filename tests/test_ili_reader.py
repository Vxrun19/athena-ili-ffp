class TestEachExampleFile:
    @staticmethod
    def _within_2pct(got, expected):
        if expected == 0:
            return got == 0
        lo, hi = int(expected * 0.98), int(expected * 1.02 + 0.999)
        return lo <= got <= hi

    def test_features_total_within_2pct_of_baseline(self, parsed):
        _fname, run, exp = parsed
        expected = exp["features_total"]
        assert self._within_2pct(len(run.features), expected), (
            f"features count drifted: got {len(run.features)}, "
            f"baseline {expected} (±2 %)"
        )

    def test_features_for_assessment_within_2pct(self, parsed):
        """Excludes cluster children — matches the PDF executive summary
        for files where that figure is known."""
        _fname, run, exp = parsed
        expected = exp["features_for_assessment"]
        got = len(run.features_for_assessment())
        assert self._within_2pct(got, expected), (
            f"features_for_assessment drifted: got {got}, baseline {expected} (±2 %)"
        )

    def test_expected_qa_flags_present(self, parsed):
        _fname, run, exp = parsed
        got_codes = {f.code for f in run.qa_flags}
        for expected_code in exp["expected_qa_codes"]:
            assert expected_code in got_codes, (
                f"missing expected QA flag {expected_code}; got {got_codes}"
            )

    def test_joint_count_above_minimum(self, parsed):
        _fname, run, exp = parsed
        assert len(run.joints) >= exp["min_joints"]

    def test_sheet_and_header_match_expectation(self, parsed):
        _fname, run, exp = parsed
        if exp["sheet"] is not None:
            assert run.sheet_name == exp["sheet"]
        assert run.header_row_idx == exp["header_idx"]

    def test_all_required_canonical_fields_in_column_map(self, parsed):
        _fname, run, _exp = parsed
        for f in _REQUIRED_FIELDS_ON_FEATURE:
            # joint_number is required; columns named "surface" / "wt_mm" etc.
            # must have resolved to a real column index.
            pass
        for f in ("abs_distance_m", "joint_number", "depth_pct_wt",
                  "length_mm", "width_mm", "surface", "wt_mm"):
            assert f in run.column_map, (
                f"required canonical field {f!r} not mapped in {run.sheet_name!r}; "
                f"mapped: {sorted(run.column_map)}"
            )

    def test_no_none_in_core_feature_fields(self, parsed):
        """Every feature must have a non-None joint_number, abs_distance, depth."""
        _fname, run, _exp = parsed
        assert run.features, "reader produced zero features"
        offenders: list[str] = []
        for f in run.features[:5000]:  # cap for very large files
            if f.joint_number is None:
                offenders.append(f"joint_number None on row {f.source_row}")
                break
            if f.abs_distance_m is None:
                offenders.append(f"abs_distance None on row {f.source_row}")
                break
            if f.depth_pct_wt is None:
                offenders.append(f"depth None on row {f.source_row}")
                break
        assert not offenders, offenders

    def test_audit_trail_populated(self, parsed):
        _fname, run, _exp = parsed
        assert run.rows_read > 0
        assert run.column_map
        # rows_filtered may legitimately be empty (e.g. Athena 2018 ML List
        # is pre-filtered upstream) — only check the dict exists.
        assert isinstance(run.rows_filtered, dict)


