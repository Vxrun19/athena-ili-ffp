class TestClockHandlesBothFormats:
    """Clock column is parsed correctly regardless of whether Excel stored it
    as a datetime.time (most NGP files) or as a string (older Athena tallies).
    """

    def test_ngp_datetime_time_path(self, reader):
        # 1YCF's clock column is `datetime.time(5, 46)` for row 2.
        run = _read_cached(reader, "1YCF_Pipeline_Listing__run2_.xlsx")
        # First feature in the file corresponds to that row.
        first = run.features[0]
        # 5:46  ->  5 + 46/60 = 5.7666...
        assert first.clock_decimal_hours == pytest.approx(5 + 46 / 60.0)

    def test_athena_2018_time_in_metal_loss_list(self, reader):
        # Metal Loss List stores `datetime.time(5, 20)` for the first row.
        run = _read_cached(
            reader, "10_inch_Kandla_to_Samakhiali__58_2_km_FR_Pipe_Tally_Rev_0.xlsx"
        )
        first = run.features[0]
        assert first.clock_decimal_hours == pytest.approx(5 + 20 / 60.0)


